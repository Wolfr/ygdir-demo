<h2 class="c-h2">Components</h2>

<div class="c-content">

    <p>Ygdir comes with a set of components. You are free to use them as you see fit. It is important to know that every Ygdir component aims to <strong>stand on its own</strong>.</p>

    <p>We recommend <strong>copying Ygdir</strong> to your local CSS for the best developer experience. It makes zero sense to have CSS locked in an NPM package that you have to combine with CSS in your own app.</p>

    <p>Given different situations, here are our recommendations</p>

    <ul>
        <li>Code experiment: use Ygdir from a CDN</li>
        <li>If you don't plan to write your own CSS: load Ygdir components via node_modules by using specific imports.</li>
        <li>Any non-trivial project: Copy Ygdir to a local SCSS folder and start customizing Ygdir.</li>
    </ul>

</div>